# Convert a list into a tuple

# Define a list containing some elements
mylist = ["python", "coding", "pandas", "django"]

# Convert the list into a tuple using the tuple() function
mytuple = tuple(mylist)

# Check the type of the variable 'mytuple' to confirm it's a tuple
print(type(mytuple))  # This will output: <class 'tuple'>

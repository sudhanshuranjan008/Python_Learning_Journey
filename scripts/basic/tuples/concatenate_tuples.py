# Concatenate multiple tuples into one

prime1 = (2, 3, 5, 7)
prime2 = (11, 13, 17, 19)
prime3 = (23, 29)

# Concatenate the tuples using the + operator
prime = prime1 + prime2 + prime3
print("The concatenated tuple is: ", prime)
